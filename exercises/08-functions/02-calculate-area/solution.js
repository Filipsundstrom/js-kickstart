// Calculate Area Exercise - Solution
// Goal: Create a function that calculates the area of a rectangle

// Define the calculateArea function
function calculateArea(width, height) {
    return width * height;
}

// Test the function with different values
console.log(calculateArea(5, 3));    // Output: 15
console.log(calculateArea(10, 7));   // Output: 70
console.log(calculateArea(2.5, 4));  // Output: 10